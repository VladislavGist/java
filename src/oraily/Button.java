package oraily;

public class Button {
    private String title;
    private String color;

    public void setTitle(String aTitle) {
        title = aTitle;
    }

    public void setColor(String aColor) {
        color = aColor;
    }

    public String getTitle() {
        return title;
    }

    public String getColor() {
        return color;
    }
}
